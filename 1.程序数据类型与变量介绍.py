# 简单打印数据
print(3 + 7)
print(2 - 1)
print("this is a chunk of text")

# 打印数据类型
print(type(2.3))
print(type(3 + 7))
print(2 - 1)
print(type("this is a chunk of text"))
print(type(2.3))

# 变量
a = 9
b = a * a - a - 1
c = a * b
print(c)

# 条件语句
